# 5. Comparação de dois números
# Peça dois números inteiros e informe qual deles é maior ou se são iguais.

numero_1 = float(input("digite aqui o primeiro numero: "))
numero_2 = float(input("digite aqui o segundo numero: "))
if numero_1 == numero_2:
    print("os nummeros sao iguais")
if numero_1 > numero_2:
    print("o numero 1 é maior que o numero 2")
else:
    print("o numero 2 é maior que o numero 1")
